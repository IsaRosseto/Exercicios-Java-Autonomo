public class ExercicioPatinhos {
    public static void main(String[] args) {

        for (int patinhos = 5; patinhos >= 0; patinhos--) {
            System.out.println("5 patinhos foram passear\r\n"
                    + "Além das montanhas\r\n"
                    + "Para brincar\r\n"
                    + "A mamãe gritou: Quá, quá, quá, quá\r\n"
                    + "Mas só " + patinhos + " patinhos voltaram de lá.\r\n"
                    + " ");

            if (patinhos == 0) {
                System.out.println("A mamãe patinha foi procurar\r\n" +
                        "Além das montanhas\r\n" +
                        "Na beira do mar\r\n" +
                        "A mamãe gritou: Quá, quá, quá, quá\r\n" +
                        "E os 5 patinhos voltaram de lá."
                );
            }
        }

        System.out.println();
    }

}

